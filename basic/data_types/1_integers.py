# Caveat: Operator precedence
# 1. Parentheses () 
# 2. Exponents **
# 3. Multiplication and Division (from left to right) * / // %
# 4. Addition and Subtraction (from left to right) + -  

print("Integers:")
print(type(2 + 4))
print(type(2 - 4))
print(type(2 * 4))
print(type(2 / 4))
print(type(2 // 4)) # 0.5
print(type(2 % 4))
print(type(2 ** 4))
